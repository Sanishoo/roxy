# === Stage 26: Добавь набор демо-команд для быстрого ручного тестирования ===
# Project: AssetRegister
def demo_run():
    """Демо-команды для ручного тестирования реестра."""
    print("=== Демо-запуск AssetRegister ===")
    # 1) Создание нескольких активов
    assets = [
        ("Сервер-01", "Иван Иванов", "В норме", "2025-06-30"),
        ("Ноутбук-02", "Мария Петрова", "Ремонт", "2025-07-15"),
        ("Принтер-03", "Алексей Сидоров", "В норме", "2025-08-20"),
    ]
    print(f"Добавлено {len(assets)} активов")

    # 2) Вывод списка с краткой информацией
    for name, owner, status, check_date in assets:
        print(f"  • {name} | Владелец: {owner} | Статус: {status} | Проверка: {check_date}")

    # 3) Поиск по владельцу
    target_owner = "Мария Петрова"
    matches = [a for a in assets if a[1] == target_owner]
    print(f"\nАктивы владельца '{target_owner}':")
    for m in matches:
        print(f"  • {m[0]} — статус: {m[2]}, проверка: {m[3]}")

    # 4) Фильтрация по статусу
    low_status = "Ремонт"
    in_repair = [a for a in assets if a[2] == low_status]
    print(f"\nАктивы в статусе '{low_status}':")
    for r in in_repair:
        print(f"  • {r[0]} — проверка должна быть проведена до {r[3]}")

    # 5) Имитация исторического события
    event_log = [
        {"актив": "Сервер-01", "событие": "Регистрация", "дата": "2024-01-15"},
        {"актив": "Ноутбук-02", "событие": "Обнаружен дефект экрана", "дата": "2025-05-10"},
    ]
    print(f"\nИсторические события ({len(event_log)}):")
    for ev in event_log:
        print(f"  • [{ev['дата']}] {ev['актив']}: {ev['событие']}")

    print("\n=== Демо-запуск завершен ===")
